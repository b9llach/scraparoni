    def __init__(self, prefer_desktop: bool = True, sticky: bool = False):
        """
        Initialize ScraparoniAgent

        Args:
            prefer_desktop: Use desktop agents by default
            sticky: Keep same agent for session (default: False for rotation)
        """
        self.prefer_desktop = prefer_desktop
        self.sticky = sticky
        self._cached_agent = None

    def get_random_agent(self) -> str:
        """Get a random user-agent string with optional stickiness"""
        if self.sticky and self._cached_agent:
            return self._cached_agent

        pool = self.DESKTOP_AGENTS if self.prefer_desktop else self.MOBILE_AGENTS
        agent = random.choice(pool)

        if self.sticky:
            self._cached_agent = agent

        return agent

    def get_headers(self, extra_headers: Dict[str, str] = None) -> Dict[str, str]:
        """
        Get realistic HTTP headers with random user-agent

        Args:
            extra_headers: Additional headers to merge

        Returns:
            Complete header dict
        """
        headers = {
            "User-Agent": self.get_random_agent(),
            "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,*/*;q=0.8",
            "Accept-Language": "en-US,en;q=0.9",
            "Accept-Encoding": "gzip, deflate, br",
            "DNT": "1",
            "Connection": "keep-alive",
            "Upgrade-Insecure-Requests": "1",
            "Sec-Fetch-Dest": "document",
            "Sec-Fetch-Mode": "navigate",
            "Sec-Fetch-Site": "none",
            "Sec-Fetch-User": "?1",
            "Cache-Control": "max-age=0",
        }

        if extra_headers:
            headers.update(extra_headers)

        return headers

    def rotate(self) -> None:
        """Force rotation by clearing cached agent"""
        self._cached_agent = None
